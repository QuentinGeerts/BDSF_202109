a = 10              # 10    
b = 5 // 2          #  2    # Division entière = on ne garde que la partie entière de la division
c = b + a           # 12
d = 5 % 2           #  1    # Modulo = reste de la division entière
e = 5 / 2.0         #  2.5
f = (e - 0.5) * a   # 20

print(a, b, c, d, e, f, sep='\n')
